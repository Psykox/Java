
package javaprimerproyecto01;

import java.util.Scanner;


public class Ejercicio1Condicionales {
    public static void main(String[] args) {
        Scanner leer = new Scanner (System.in);
        System.out.println("Ingrese un numero entero para determinar si es par o impar");
        int numer1 = leer.nextInt();
        
        if (numer1%2==0){
            System.out.println("El numero es par "+numer1);
        }else {
            System.out.println("El numero es impar "+numer1);
        }
        
        
        
        
        
        
        
        
        
        
        
        
    }
}
